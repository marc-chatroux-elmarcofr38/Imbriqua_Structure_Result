#[derive(Builder, Debug, Clone)]
pub struct Definitions {
    #[builder(setter(into))]
    pub name: dc::String,
    #[builder(setter(into))]
    pub target_namespace: dc::String,
    #[builder(setter(into), default = "String::from(\"http://www.w3.org/1999/XPath\")")]
    pub expression_language: dc::String,
    #[builder(setter(into), default = "String::from(\"http://www.w3.org/2001/XMLSchema\")")]
    pub type_language: dc::String,
    #[builder(setter(into, strip_option), default)]
    pub imports: Option<Vec<Import>>,
    #[builder(setter(into, strip_option), default)]
    pub extensions: Option<Vec<Extension>>,
    #[builder(setter(into, strip_option), default)]
    pub relationships: Option<Vec<Relationship>>,
    #[builder(setter(into, strip_option), default)]
    pub root_elements: Option<Vec<RootElement>>,
    #[builder(setter(into, strip_option), default)]
    pub diagrams: Option<Vec<i8>>,
    #[builder(setter(into))]
    pub exporter: dc::String,
    #[builder(setter(into))]
    pub exporter_version: dc::String,
}

